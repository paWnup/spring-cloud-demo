/**
 * eureka服务注册中心
 *
 * @author puan
 * @date 2018-11-07 17:43
 **/
@SpringBootApplication
@EnableEurekaServer
public class RegistryApplication {

    public static void main(String[] args) {
        SpringApplication.run(RegistryApplication.class, args);
    }
}
